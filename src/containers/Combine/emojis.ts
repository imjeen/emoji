const emojis = emojiData.map(emoji => {
  const name = emoji.codes
    .map((c: number) => c.toString(16))
    .filter((c: string) => c !== 'fe0f')
    .join('_');
  return {
    name,
    ...emoji,
  };
});

export { emojis as default };

/**
 * 
 *
 * @param {TypeEmojis[0]} emoji1
 * @param {TypeEmojis[0]} emoji2
 */
export function getCombineData(emoji1: typeof emojis[0], emoji2: typeof emojis[0]) {
  const getUnicode = (list: number[]) => list.map(c => 'u' + c.toString(16)).join('-');

  const u1 = getUnicode(emoji1.codes),
    u2 = getUnicode(emoji2.codes);
  const pathname = `${emoji1.update}/${u1}`;

  return { name: `${u1}_${u2}.png`, pathname, url: `${EMOJI_API_PATH}${pathname}/${u1}_${u2}.png` };
}
